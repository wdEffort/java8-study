package com.ezen.example;

/**
 * 숫자 리터럴 - 02
 * 8진수, 16진수 표기
 * 
 * @author yoman
 * 
 * 2019. 09. 22
 * 
 * 정수 리터럴은 일반적인 표기법 이외에도
 * 8진수, 16진수와 같은 표기법이 있습니다.
 * 
 * 8진수 : 숫자 앞에 0을 붙입니다.
 * 16진수 : 숫자앞에 0x를 붙입니다.
 */
public class NumberLiteralSample02 {

	public static void main(String[] args) {
		// 정수 리터럴 실습 예제 - 02
		System.out.println(10); // 10진수
		System.out.println(010); // 8진수
		System.out.println(0x10); // 16진수
	}

}
