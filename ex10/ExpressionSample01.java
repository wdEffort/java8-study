package com.ezen.example;

/**
 * 식과 연산자
 * 식과 연산자의 개념
 * 
 * @author yoman
 * 
 * 2019. 09. 24
 * 
 * 컴퓨터는 다양한 일을 '계산'해서 처리합니다.
 * Java에서 식의 대부분은 연산자와 피연산자를 조합해서 만듭니다.
 * 연산자 : 연산하는 것(Operator)
 * 피연산자 : 연산 대상(Operand)
 */
public class ExpressionSample01 {

	public static void main(String[] args) {
		// 식의 값을 출력하는 예제
		System.out.println("1 + 2는 " + (1 + 2) + "입니다."); // 덧셈
		System.out.println("3 * 4는 " + (3 * 4) + "입니다."); // 곱셈
	}

}
