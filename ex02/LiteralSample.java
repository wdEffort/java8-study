package com.ezen.example;

/**
 * 리터럴(Literal)
 * 
 * @author yoman
 * 
 * 2019. 09. 22
 * 
 * 리터럴이란?
 * 일정한 '값'을 나타내는 데 이용되는 Java의 단어 같은 것들을 의미합니다.
 * 
 * 주요 리터럴
 * 1. 문자 리터럴
 * 2. 문자열 리터럴
 * 3. 숫자(정수, 부동 소수) 리터럴
 */
public class LiteralSample {

	public static void main(String[] args) {
		// 리터럴 실습 예제
		System.out.println('A'); // 문자를 출력합니다.
		System.out.println("Java 세상에 오신 것을 환영합니다!"); // 문자열을 출력합니다.
		System.out.println(100); // 숫자를 출력합니다.
		System.out.println(2.5); // 실수를 출력합니다.
	}

}
